#!/usr/bin/env python3
"""
Generate LAUNCH_READINESS.md — a living checklist of skills to apply before shipping.

Default filter: priority=core OR supporting, stage IN (harden, launch).
Override via env vars or CLI flags.

Usage:
  python3 tools/generate_launch_readiness.py
  python3 tools/generate_launch_readiness.py --stages harden launch scale
  python3 tools/generate_launch_readiness.py --priority core --stages launch
"""

from __future__ import annotations
import argparse
import datetime as dt
import json
from pathlib import Path

ROOT = Path(__file__).resolve().parent.parent
DB_PATH = ROOT / "skills-database.json"
OUT_PATH = ROOT / "LAUNCH_READINESS.md"

STAGE_ORDER = ["experiment", "build", "harden", "launch", "scale"]
STAGE_EMOJI = {
    "experiment": "🧪",
    "build":      "🔨",
    "harden":     "🛡️",
    "launch":     "🚀",
    "scale":      "📈",
}
STAGE_DESC = {
    "experiment": "Pre-commitment: design, spec, evaluate",
    "build":      "Active construction: implement features",
    "harden":     "Pre-ship: security, edge cases, coverage",
    "launch":     "Ship mechanics: CI/CD, containers, release",
    "scale":      "Post-launch: performance, infra, analytics",
}


def main() -> None:
    p = argparse.ArgumentParser()
    p.add_argument("--stages", nargs="+", default=["harden", "launch"],
                   choices=STAGE_ORDER, metavar="STAGE")
    p.add_argument("--priority", nargs="+", default=["core", "supporting"],
                   choices=["core", "supporting"], metavar="PRIORITY")
    args = p.parse_args()

    db = json.loads(DB_PATH.read_text())
    skills = [
        s for s in db["skills"]
        if s.get("stage") in args.stages and s.get("priority") in args.priority
    ]

    # Group by stage (in defined order), then category
    by_stage: dict[str, dict[str, list[dict]]] = {st: {} for st in args.stages}
    for s in skills:
        st = s["stage"]
        cat = s["category"]
        by_stage[st].setdefault(cat, []).append(s)

    lines: list[str] = [
        "# Launch Readiness Checklist",
        "",
        f"> Auto-generated from `skills-database.json` v{db['version']} · {dt.date.today().isoformat()}",
        f"> Filter: stages={args.stages}  priority={args.priority}",
        f"> **{len(skills)} skills** across {len(args.stages)} stages",
        "",
        "Copy this checklist into your project. Check off each skill as you apply it.",
        "Re-run `python3 tools/generate_launch_readiness.py` to regenerate after library updates.",
        "",
    ]

    for st in args.stages:
        cats = by_stage[st]
        if not cats:
            continue
        emoji = STAGE_EMOJI[st]
        desc = STAGE_DESC[st]
        count = sum(len(v) for v in cats.values())
        lines += [
            f"## {emoji} {st.capitalize()} — {desc}",
            f"*{count} skills*",
            "",
        ]
        for cat in sorted(cats):
            lines.append(f"### {cat.replace('-', ' ').title()}")
            lines.append("")
            for s in sorted(cats[cat], key=lambda x: (x["priority"] != "core", x["name"])):
                badge = "🔑" if s["priority"] == "core" else "  "
                lines.append(f"- [ ] {badge} **`{s['name']}`** — {s['description'][:100]}")
            lines.append("")

    lines += [
        "---",
        "",
        "**Key:** 🔑 = core priority (must-apply) · unmarked = supporting (apply if relevant)",
        "",
        f"Generated by `tools/generate_launch_readiness.py` from `skills-database.json` v{db['version']}",
    ]

    OUT_PATH.write_text("\n".join(lines) + "\n")
    print(f"Wrote {OUT_PATH.name} ({len(skills)} skills, {len(lines)} lines)")


if __name__ == "__main__":
    main()
